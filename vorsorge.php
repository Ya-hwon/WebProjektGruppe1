    <h2 id="Vorsorgevollmacht">1. Vorsorgevollmacht</h2>
    <p class="vorsorgecontent">Solange Sie noch geschäftsfähig sind, haben Sie jederzeit die Möglichkeit selbst zu bestimmen, wer Ihre
        Angelegenheiten regeln soll, falls Sie hierzu gesundheitlich nicht mehr in der Lage sein sollten. Durch eine
        <b>Vorsorgevollmacht</b> können Sie eine oder mehrere Personen, denen Sie uneingeschränkt vertrauen, mit Ihrer rechtlichen
        Vertretung beauftragen. Sofern Sie mehrere Personen bevollmächtigen, empfiehlt es sich jedem eine <b>Einzelvollmacht</b>
        zu erteilen. Bedenken Sie, dass Sie gerade für den Fall der eigenen Hilflosigkeit diese Vorsorgevollmacht erteilen
        und es Ihnen dann vielleicht nicht möglich ist, die bevollmächtigte Person zu überwachen. Deshalb sollten Sie bei
        der Auswahl des oder der Bevollmächtigten besonders sorgfältig sein.<br /><br /> Es ist auch möglich, eine zweite Person nur
        mit der Aufgabe zu bevollmächtigen, die Tätigkeiten des Erstbevollmächtigten zu kontrollieren. Falls Sie in einem
        Heim wohnen, sollten Sie keine Person bevollmächtigen, die dort arbeitet oder in einer anderen engen Beziehung zu
        dieser Einrichtung steht.<br />
        In der <b>Vorsorgevollmacht</b> sollten Sie schriftlich detailliert festlegen, in welchem Rahmen Sie der Bevollmächtigte
        vertreten darf. Sie können eine umfassende Vorsorgevollmacht erteilen, welche zur Entscheidung über alle denkbaren
        Lebensereignisse befugt und deshalb am zweckmäßigsten ist. Es können aber auch nur Teilbereiche übertragen werden,
        wie beispielsweise die Vertretung in finanziellen Angelegenheiten oder Entscheidungen über ärztliche
        Behandlungsmaßnahmen. In der Vorsorgevollmacht sollte klar bestimmt sein, dass diese auch über den Tod
        hinaus gültig ist, damit der Bevollmächtigte so lange handlungsfähig bleibt, bis dieser von den Erben abgelöst wird.<br /><br />
        Wir empfehlen Ihnen, Ihre Vorsorgevollmacht vor einem Notar zu erklären und beurkunden zu lassen. Dort erhalten
        Sie auch eine fachkundige Beratung. Die Höhe der anfallenden Gebühr richtet sich nach Ihren Vermögensverhältnissen.<br />
        Sie können auch die von Ihnen erstellte Vorsorgevollmacht bei der Betreuungsbehörde des Landratsamtes
        unterschreiben und dort Ihre Unterschrift gegen eine Gebühr von 10 € beglaubigen lassen. Allerdings gibt es keine
        Garantie, dass diese beglaubigte Vorsorgevollmacht überall anerkannt wird. Die Banken erkennen in der Regel
        lediglich Vorsorgevollmachten an, die auf ihren eigenen Vordrucken abgegeben oder notariell beurkundet wurden.
        Für eine Beglaubigung der Vorsorgevollmacht bei der Betreuungsbehörde sollten Sie zuvor einen Termin vereinbaren.<br />
        Die von Ihnen erteilte Vorsorgevollmacht soll erst dann wirksam werden, wenn Sie selbst krankheits- oder
        behinderungsbedingt Ihre Angelegenheiten nicht mehr regeln können. Sie sollten deshalb dafür sorgen, dass die nur
        im Original gültige Vollmachtsurkunde auch nur in diesem Fall dem Bevollmächtigten zugeleitet wird. Denn eine
        sicher aufbewahrte Vorsorgevollmacht schützt vor Missbrauch, eine unauffindbare Vorsorgevollmacht kann nicht
        wirksam werden.<br /><br /> Es besteht auch die Möglichkeit der Hinterlegung bei Vertrauenspersonen (z.B. Rechtsanwalt, Notar).<br />
        Falls Sie eine Vorsorgevollmacht erteilen, sollten Sie den Bevollmächtigten auch davon in Kenntnis setzen.<br />
        Beim Zentralen Vorsorgeregister der Bundesnotarkammer, Postfach 08 01 51 in 10001 Berlin, Internetadresse:
        <a target="_blank" href="https://www.vorsorgeregister.de/">www.vorsorgeregister.de</a>, können Sie Ihre Vorsorgevollmacht gegen eine Gebühr (ab 13 €) registrieren lassen.
        In einem Betreuerbestellungsverfahren wird vom Betreuungsgericht stets im Vorsorgeregister abgefragt, ob bereits
        eine Vorsorgevollmacht erteilt wurde.<br /><br />
        Der <b>Widerruf</b> einer erteilten Vorsorgevollmacht ist jederzeit möglich, so lange Sie noch geschäftsfähig sind.<br />
        Außerhalb der Vorsorgevollmacht können Sie schriftlich festlegen, was von Ihrer Vertrauensperson beachtet werden
        soll, damit Sie auch im Falle der Hilflosigkeit Ihren persönlichen Lebensstil weitestgehend beibehalten können.<br />
        Diese Handlungsanweisungen an den Bevollmächtigten können beispielsweise Ihre gewünschte Versorgung im Alter zum
        Inhalt haben oder Sie können festlegen, wie mit Ihrem Haus- und Grundeigentum verfahren werden soll. Beabsichtigen
        Sie, dem Bevollmächtigten seine Aufwendungen zu ersetzen oder ihn für seine Arbeit zu entlohnen, sollten Sie dies
        ebenfalls schriftlich festhalten. Solche Regelungen im Innenverhältnis zwischen Vollmachtgeber und Bevollmächtigtem
        werden als Geschäftsbesorgungsvertrag bezeichnet.<br />
        Soll die Vorsorgevollmacht auch zu schwerwiegenden Entscheidungen in den Bereichen der Gesundheit und der
        persönlichen Bewegungsfreiheit ermächtigen, muss dies in der Vollmachtsurkunde unter Hinweis auf die §§ 1904 und
        1906 des Bürgerlichen Gesetzbuches - BGB - aufgeführt sein. Der Bevollmächtigte benötigt jedoch für die Anwendung
        von freiheitsentziehenden Maßnahmen grundsätzlich die vorherige Genehmigung des Amtsgerichts. Für eine Einwilligung
        in eine risikoreiche Arztbehandlung besteht keine Genehmigungspflicht, wenn sich Arzt und Bevollmächtigter darüber
        einig sind, dass die Erteilung dem Willen des Vollmachtgebers entspricht.<br /> Verweigert der Bevollmächtigte seine
        Einwilligung in eine vom Arzt angebotene Behandlung, mit der Folge, dass die begründete Gefahr besteht, dass der
        Vollmachtgeber auf Grund des Unterbleibens oder des Abbruchs der Maßnahme stirbt, ist nur eine Genehmigung durch
        das Amtsgericht erforderlich, wenn sich Arzt und Bevollmächtigter nicht einig sind, ob dies auch dem Willen des
        Vollmachtgebers entspricht.<br />
        Alle anderen Handlungen des Bevollmächtigten unterliegen lediglich Ihrer Kontrolle. Sollten Sie hierzu aus
        gesundheitlichen Gründen nicht mehr in der Lage sein, kann vom Betreuungsgericht ein Kontrollbetreuer eingesetzt
        werden, wenn dieses eine Überwachung für erforderlich erachtet.<br />
        Durch die Erteilung einer umfassenden Vorsorgevollmacht können Sie vermeiden, dass für Sie vom Betreuungsgericht
        ein Betreuer bestellt werden muss.<br />
    </p>

    <h2 id="Betreuungsverfügung">2. Betreuungsverfügung</h2>
    <p class="vorsorgecontent">
        Wenn Sie keine Vorsorgevollmacht erteilt haben und Ihre Angelegenheiten nicht mehr regeln können, muss für Sie
        ein <b>gesetzlicher Betreuer</b> bestellt werden. Durch eine <b>Betreuungsverfügung</b> können Sie Ihre Vorstellungen
        hinsichtlich der gesetzlichen Betreuung einbringen. So können Sie festlegen, wen Sie sich als Betreuer wünschen.
        Sie können auch erklären, wer als Betreuer für Sie keinesfalls bestellt werden soll.<br />
        Falls Sie genaue Vorstellungen haben, wie der Betreuer Ihre Betreuung führen soll, können Sie diese persönlichen
        Wünsche in die Betreuungsverfügung mit aufnehmen. Dies können Regelungen sein, wie zum Beispiel Ihre finanziellen
        Mittel verwendet werden sollen oder wo und wie Sie im Alter wohnen und gepflegt werden wollen. An Ihre Wünsche sind
        dann das Betreuungsgericht und ein bestellter Betreuer gebunden.<br /><br />
        Wir empfehlen die Betreuungsverfügung schriftlich zu verfassen und so zu hinterlegen, dass diese im Falle Ihrer
        Betreuungsbedürftigkeit auch dem Betreuungsgericht zugeleitet wird. Eine Betreuungsverfügung ist auch dann gültig,
        wenn Sie zum Zeitpunkt der Verfassung nicht mehr geschäftsfähig waren.<br />
        Ihre Unterschrift unter eine Betreuungsverfügung kann von der Betreuungsbehörde des Landratsamtes Heidenheim gegen
        eine Gebühr von 10€ beglaubigt werden. Sie sollten für eine Beglaubigung bei der Betreuungsbehörde einen Termin
        vereinbaren. Auch die Betreuungsverfügung können Sie beim Zentralen Vorsorgeregister der Bundesnotarkammer gegen
        eine Gebühr registrieren lassen (Anschrift siehe unter Vollmacht).<br />
    </p>

    <h2 id="Patientenverfügung">3. Patientenverfügung</h2>
    <p class="vorsorgecontent">Grundsätzlich ist für jede ärztliche Behandlung die <b>Zustimmung des Betroffenen</b> erforderlich.<br />
        Für den Fall, dass Sie über eine vom Arzt angebotene Behandlung krankheitsbedingt nicht mehr entscheiden können,
        ist es Ihnen als Erwachsener heute schon möglich, Ihre Behandlungswünsche vorsorglich in einer Patientenverfügung
        niederzuschreiben. Diese Situation kann sehr rasch eintreten, beispielsweise wenn Sie nach einem Schlaganfall nicht
        mehr sprechen können. Nur wenn Sie in Lebensgefahr sind und ein schnelles Handeln geboten ist, kann und darf Ihr
        Arzt ohne eine förmliche Einwilligung die erforderliche ärztliche Behandlung durchführen. Ansonsten muss die
        Entscheidung Ihres Betreuers oder Bevollmächtigten eingeholt werden.<br />
        In dem am 01.09.2009 in Kraft getretenen <b>Dritten Gesetz</b> zur Änderung des Betreuungsrechts wurde festgelegt, dass
        Betreuer und Bevollmächtigte an Ihre Willensbestimmung in Form einer Patientenverfügung gebunden sind. Deren
        Aufgabe ist es auf der Grundlage der Patientenverfügung Ihren Behandlungswillen festzustellen und ihm Ausdruck
        und Geltung zu verschaffen. Können sich Arzt und Betreuer oder Bevollmächtigte über den Patientenwillen nicht
        einigen, muss deren Entscheidung vom Betreuungsgericht genehmigt werden.<br /></p>
    <h3>Form, Inhalt und Verwahrung einer Patientenverfügung</h3>
    <div class="vorsorgecontent">
        <ul>
            <li>
                <p>Eine Patientenverfügung muss schriftlich verfasst und vom Aussteller eigenhändig durch Namensunterschrift oder durch ein notariell beglaubigtes Handzeichen unterzeichnet sein.<br /></p>
            </li>
            <li>
                <p>Die Festlegungen über die künftige ärztliche Behandlung in einer Patientenverfügung sollten sich auf die zu erwartende Behandlungssituation beziehen, beispielsweise einen Komazustand oder eine unheilbare Krebserkrankung.<br /></p>
            </li>
            <li>
                <p>Sie sollten so konkret wie möglich bestimmen, wie Sie in diesen Situationen ärztlich behandelt werden wollen. Beispielsweise können Sie Schmerztherapie, künstliche Ernährung oder Beatmung, Flüssigkeitszufuhr usw. wünschen oder ablehnen.<br /></p>
            </li>
            <li>
                <p>Patientenverfügungen können jederzeit formlos widerrufen oder geändert werden, insofern der Verfügende noch geistig in der Lage ist, über ärztliche Behandlungsmaßnahmen zu entscheiden. Es ist empfehlenswert, eine Patientenverfügung in bestimmten Zeitabständen (z.B. jährlich) zu erneuern oder zu bestätigen. So kann man im eigenen Interesse regelmäßig überprüfen, ob die einmal getroffenen Festlegungen noch gelten oder eventuell konkretisiert oder abgeändert werden sollten.<br /></p>
            </li>
            <li>
                <p>Eine Patientenverfügung sollte so verwahrt werden, dass insbesondere Ihr Arzt, Ihr Bevollmächtigter oder Ihr Betreuer möglichst schnell Kenntnis von der Existenz und vom Hinterlegungsort erlangen können. Es ist sinnvoll, einen Hinweis bei sich zu tragen, wo die Patientenverfügung aufbewahrt wird. Bei Aufnahme in ein Krankenhaus oder Pflegeheim sollten Sie auf Ihre Patientenverfügung hinweisen.<br /></p><br />
            </li>
        </ul>
    </div>

    <h3>Hilfen bei der Erstellung einer Patientenverfügung</h3>
    <p class="vorsorgecontent">
        Am besten lassen Sie sich von Ihrem Arzt beraten, bevor Sie eine schriftliche Patientenverfügung abfassen.
        Eine fachkundige Beratung kann Ihnen helfen, Widersprüche zwischen einzelnen Festlegungen zu vermeiden. Das
        Klinikum Heidenheim bietet auch regelmäßig Informationsveranstaltungen an.<br />
    </p>

